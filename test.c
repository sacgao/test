unsigned short 	DS6700_crc16(unsigned short  crc, unsigned char data)
{
	return 	((((crc & 0xff) ^ crc_hi[(crc >> 8) ^ data]) << 8) | crc_lo[(crc >> 8) ^ data]);
}

unsigned short  DS6700_crc16_block(unsigned char *buf, unsigned short  len)
{
	unsigned short crc = 0xffff;

	while(len)
	{
		crc = DS6700_crc16(crc, *buf++);
		len--;	
	}

	return 	crc;
}

unsigned short DS6700_crc16_multi(unsigned short crc, unsigned char *buf, unsigned short len)
{
	while(len--)
	{
		crc = DS6700_crc16(crc, *buf++);
	}

	return crc;
}


